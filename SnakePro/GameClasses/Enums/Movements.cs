namespace WebApplication2.GameClasses.Enums;

/// <summary>
/// Represents the possible movement directions.
/// </summary>
public enum Movements
{
    /// <summary>
    /// Represents no movement.
    /// </summary>
    None,

    /// <summary>
    /// Represents upward movement.
    /// </summary>
    Up,

    /// <summary>
    /// Represents downward movement.
    /// </summary>
    Down,

    /// <summary>
    /// Represents leftward movement.
    /// </summary>
    Left,

    /// <summary>
    /// Represents rightward movement.
    /// </summary>
    Right
}