export class User {
  /**
   * Gets or sets the unique identifier for the user.
   */
  uid?: string;

  /**
   * Gets or sets the display name of the user.
   */
  displayName?: string;

  /**
   * Gets or sets the email of the user.
   */
  email?: string;

  /**
   * Gets or sets the score of the user.
   */
  score?: number;
}
